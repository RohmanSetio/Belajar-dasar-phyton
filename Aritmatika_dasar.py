a = 10
b = 5

#penambahan
hasil = a + b
print(a,"+",b,"=",hasil)

#pengurangan
hasil = a - b 
print(a,"-",b,"=",hasil)

#perkalian
hasil = a * b
print(a,"*",b,"=",hasil)

#pembagian
hasil = a / b
print(a,"/",b,"=",hasil)

#eksponen
hasil = a ** b
print(a,"**",b,"=",hasil)

#floor divison
hasil = a // b
print(a,"//",b,"=",hasil)

#modulus
hasil = a % b
print(a,"%",b,"=",hasil)

